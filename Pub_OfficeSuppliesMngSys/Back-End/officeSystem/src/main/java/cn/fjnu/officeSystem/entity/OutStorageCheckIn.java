package cn.fjnu.officeSystem.entity;

import java.util.Date;

public class OutStorageCheckIn {
	
	private String id;

	private Date outStorageDate;// 出库日期

	private Short outStorageTypeId;// 出库类型

	private String recipienter;// 领用人id

	private String departmentId;// 领用部门id

	private String operaterId;// 制单人（操作员）

	private String applyId;// 物品清单--领用申请或借用申请（可以批量出库）

	private Long totalNumber;// 合计数量

	private Double totalMonney;// 合计金额

	private String remark;// 备注

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Date getOutStorageDate() {
		return outStorageDate;
	}

	public void setOutStorageDate(Date outStorageDate) {
		this.outStorageDate = outStorageDate;
	}

	public Short getOutStorageTypeId() {
		return outStorageTypeId;
	}

	public void setOutStorageTypeId(Short outStorageTypeId) {
		this.outStorageTypeId = outStorageTypeId;
	}

	public String getRecipienter() {
		return recipienter;
	}

	public void setRecipienter(String recipienter) {
		this.recipienter = recipienter;
	}

	public String getDepartmentId() {
		return departmentId;
	}

	public void setDepartmentId(String departmentId) {
		this.departmentId = departmentId;
	}

	public String getOperaterId() {
		return operaterId;
	}

	public void setOperaterId(String operaterId) {
		this.operaterId = operaterId;
	}

	public String getApplyId() {
		return applyId;
	}

	public void setApplyId(String applyId) {
		this.applyId = applyId;
	}

	public Long getTotalNumber() {
		return totalNumber;
	}

	public void setTotalNumber(Long totalNumber) {
		this.totalNumber = totalNumber;
	}

	public Double getTotalMonney() {
		return totalMonney;
	}

	public void setTotalMonney(Double totalMonney) {
		this.totalMonney = totalMonney;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public OutStorageCheckIn(String id, Date outStorageDate, Short outStorageTypeId, String recipienter,
			String departmentId, String operaterId, String applyId, Long totalNumber, Double totalMonney,
			String remark) {
		super();
		this.id = id;
		this.outStorageDate = outStorageDate;
		this.outStorageTypeId = outStorageTypeId;
		this.recipienter = recipienter;
		this.departmentId = departmentId;
		this.operaterId = operaterId;
		this.applyId = applyId;
		this.totalNumber = totalNumber;
		this.totalMonney = totalMonney;
		this.remark = remark;
	}

	@Override
	public String toString() {
		return "OutStorageCheckIn [id=" + id + ", outStorageDate=" + outStorageDate + ", outStorageTypeId="
				+ outStorageTypeId + ", recipienter=" + recipienter + ", departmentId=" + departmentId + ", operaterId="
				+ operaterId + ", applyId=" + applyId + ", totalNumber=" + totalNumber + ", totalMonney=" + totalMonney
				+ ", remark=" + remark + "]";
	}

	public OutStorageCheckIn() {
		super();
		// TODO Auto-generated constructor stub
	}

}
