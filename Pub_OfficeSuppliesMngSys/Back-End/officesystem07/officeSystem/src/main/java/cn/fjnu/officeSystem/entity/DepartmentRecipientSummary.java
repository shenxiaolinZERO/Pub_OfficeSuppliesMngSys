package cn.fjnu.officeSystem.entity;

public class DepartmentRecipientSummary {//部门领用汇总表
	
    private String id;
	
	private String departmentId;//领用部门id
	
	private String outStorageCheckInId;//登记编号(出库)
	
	private String itemId;//物品编号
	
	private String itemName;//物品名称
	
	private String spec;//规格型号
	
	private String itemTypeId;//物品分类id
	
	private String measureUnitId;//单位id
	
	private Double price;//单价
	
	private Long number;//数量
	
	private Double monney;//金额
	
	private Short outStorageId;//出库类型id
	
	private String remark;//备注

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getDepartmentId() {
		return departmentId;
	}

	public void setDepartmentId(String departmentId) {
		this.departmentId = departmentId;
	}

	public String getOutStorageCheckInId() {
		return outStorageCheckInId;
	}

	public void setOutStorageCheckInId(String outStorageCheckInId) {
		this.outStorageCheckInId = outStorageCheckInId;
	}

	public String getItemId() {
		return itemId;
	}

	public void setItemId(String itemId) {
		this.itemId = itemId;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public String getSpec() {
		return spec;
	}

	public void setSpec(String spec) {
		this.spec = spec;
	}

	public String getMeasureUnitId() {
		return measureUnitId;
	}

	public void setMeasureUnitId(String measureUnitId) {
		this.measureUnitId = measureUnitId;
	}

	public Double getPrice() {
		return price;
	}

	public void setPrice(Double price) {
		this.price = price;
	}

	public Long getNumber() {
		return number;
	}

	public void setNumber(Long number) {
		this.number = number;
	}

	public Double getMonney() {
		return monney;
	}

	public void setMonney(Double monney) {
		this.monney = monney;
	}

	public Short getOutStorageId() {
		return outStorageId;
	}

	public void setOutStorageId(Short outStorageId) {
		this.outStorageId= outStorageId;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public String getItemTypeId() {
		return itemTypeId;
	}

	public void setItemTypeId(String itemTypeId) {
		this.itemTypeId = itemTypeId;
	}

	@Override
	public String toString() {
		return "DepartmentRecipientSummary [id=" + id + ", departmentId=" + departmentId + ", outStrorageCheckInId="
				+ outStorageCheckInId + ", itemId=" + itemId + ", itemName=" + itemName + ", spec=" + spec
				+ ", itemTypeId=" + itemTypeId + ", measureUnitId=" + measureUnitId + ", price=" + price
				+ ", number=" + number + ", monney=" + monney + ", outStorageId=" + outStorageId + ", remark=" + remark
				+ "]";
	}

	public DepartmentRecipientSummary(String id, String departmentId, String outStorageCheckInId, String itemId,
			String itemName, String spec, String itemTypeId, String measureUnitId, Double price, Long number,
			Double monney, Short outStorageId, String remark) {
		super();
		this.id = id;
		this.departmentId = departmentId;
		this.outStorageCheckInId = outStorageCheckInId;
		this.itemId = itemId;
		this.itemName = itemName;
		this.spec = spec;
		this.itemTypeId = itemTypeId;
		this.measureUnitId = measureUnitId;
		this.price = price;
		this.number = number;
		this.monney = monney;
		this.outStorageId = outStorageId;
		this.remark = remark;
	}

	public DepartmentRecipientSummary() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	

}
