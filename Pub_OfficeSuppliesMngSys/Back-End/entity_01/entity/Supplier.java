package cn.fjnu.officeSystem.entity;

public class Supplier {
	
	private String id;
	
	private String fullName;//供应商全称
	
	private String shortName;//供应商简称
	
	private String supplierTypeName;//供应商类型
	
	private String linkman;//联系人
	
	private String cellPhoneNumber;//手机号码
	
	private String phone;//联系电话
	
	private String fax;//传真
	
	private String postcode;//邮编
	
	private String address;//地址
	
	private Short isValid;//是否有效
	
	private String remark;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getShortName() {
		return shortName;
	}

	public void setShortName(String shortName) {
		this.shortName = shortName;
	}

	public String getSupplierTypeName() {
		return supplierTypeName;
	}

	public void setSupplierTypeName(String supplierTypeName) {
		this.supplierTypeName = supplierTypeName;
	}

	public String getLinkman() {
		return linkman;
	}

	public void setLinkman(String linkman) {
		this.linkman = linkman;
	}

	public String getCellPhoneNumber() {
		return cellPhoneNumber;
	}

	public void setCellPhoneNumber(String cellPhoneNumber) {
		this.cellPhoneNumber = cellPhoneNumber;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public String getPostcode() {
		return postcode;
	}

	public void setPostcode(String postcode) {
		this.postcode = postcode;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public Short getIsValid() {
		return isValid;
	}

	public void setIsValid(Short isValid) {
		this.isValid = isValid;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	@Override
	public String toString() {
		return "Supplier [id=" + id + ", fullName=" + fullName + ", shortName=" + shortName + ", supplierTypeName="
				+ supplierTypeName + ", linkman=" + linkman + ", cellPhoneNumber=" + cellPhoneNumber + ", phone="
				+ phone + ", fax=" + fax + ", postcode=" + postcode + ", address=" + address + ", isValid=" + isValid
				+ ", remark=" + remark + "]";
	}

	public Supplier(String id, String fullName, String shortName, String supplierTypeName, String linkman,
			String cellPhoneNumber, String phone, String fax, String postcode, String address, Short isValid,
			String remark) {
		super();
		this.id = id;
		this.fullName = fullName;
		this.shortName = shortName;
		this.supplierTypeName = supplierTypeName;
		this.linkman = linkman;
		this.cellPhoneNumber = cellPhoneNumber;
		this.phone = phone;
		this.fax = fax;
		this.postcode = postcode;
		this.address = address;
		this.isValid = isValid;
		this.remark = remark;
	}

	public Supplier() {
		super();
		// TODO Auto-generated constructor stub
	}
	
	
	

}
