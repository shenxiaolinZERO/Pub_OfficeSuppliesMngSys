package cn.fjnu.officeSystem.entity;

public class ItemClassificationSummary {//物品分类汇总表
	
	private String id;
	
	private String itemId;//物品编号
	
	private String itemName;//物品名称
	
	private String itemTypeName;//物品分类
	
	private String spec;//规格型号
	
	private String measureUnitName;//单位
	
	private Double price;//单价（如果下次录入的单价不一样，还是再生成一条记录）
	
	private Long inventory;//库存数量
	
	private Double inventoryMonney;//库存金额
	
	private String remark;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getItemId() {
		return itemId;
	}

	public void setItemId(String itemId) {
		this.itemId = itemId;
	}

	public String getItemName() {
		return itemName;
	}

	public void setItemName(String itemName) {
		this.itemName = itemName;
	}

	public String getItemTypeName() {
		return itemTypeName;
	}

	public void setItemTypeName(String itemTypeName) {
		this.itemTypeName = itemTypeName;
	}

	public String getSpec() {
		return spec;
	}

	public void setSpec(String spec) {
		this.spec = spec;
	}

	public String getMeasureUnitName() {
		return measureUnitName;
	}

	public void setMeasureUnitName(String measureUnitName) {
		this.measureUnitName = measureUnitName;
	}

	public Double getPrice() {
		return price;
	}

	public void setPrice(Double price) {
		this.price = price;
	}

	public Long getInventory() {
		return inventory;
	}

	public void setInventory(Long inventory) {
		this.inventory = inventory;
	}

	public Double getInventoryMonney() {
		return inventoryMonney;
	}

	public void setInventoryMonney(Double inventoryMonney) {
		this.inventoryMonney = inventoryMonney;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public ItemClassificationSummary(String id, String itemId, String itemName, String itemTypeName, String spec,
			String measureUnitName, Double price, Long inventory, Double inventoryMonney, String remark) {
		super();
		this.id = id;
		this.itemId = itemId;
		this.itemName = itemName;
		this.itemTypeName = itemTypeName;
		this.spec = spec;
		this.measureUnitName = measureUnitName;
		this.price = price;
		this.inventory = inventory;
		this.inventoryMonney = inventoryMonney;
		this.remark = remark;
	}

	public ItemClassificationSummary() {
		super();
		// TODO Auto-generated constructor stub
	}

	@Override
	public String toString() {
		return "ItemClassificationSummary [id=" + id + ", itemId=" + itemId + ", itemName=" + itemName
				+ ", itemTypeName=" + itemTypeName + ", spec=" + spec + ", measureUnitName=" + measureUnitName
				+ ", price=" + price + ", inventory=" + inventory + ", inventoryMonney=" + inventoryMonney + ", remark="
				+ remark + "]";
	}
	
	

}
